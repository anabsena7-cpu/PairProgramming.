// 1) Condicional: if, else if, else
let idade = 20; // Define a idade de uma pessoa

if (idade < 12) {
  console.log("Criança"); // Se for menor que 12, imprime "Criança"
} else if (idade < 18) {
  console.log("Adolescente"); // Se for menor que 18, imprime "Adolescente"
} else {
  console.log("Adulto"); // Caso contrário, é adulto
}